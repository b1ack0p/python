# Math Operators
#----------------------
#   Symbol  |   Name
#----------------------
#   +       |   Sum
#   -       |   Subtraction
#   *       |   Multiply
#   /       |   Divide
#   **      |   Exponential form (uslu sayilar)
#   //      |   Full divide (tam bolme) - no floating result
#   %       |   Mod

print(2*0.5)
#output: 1.0
print(1/2)
#output: 0.5
print(3*5+7-1/4)
#output: 21.75
print(5**2)
#output: 25
print(10%2)
#output: 0
print(7%5)
#output: 2
print(10/3)
#output: 3.3333333333333335
print(10//3)
#output: 3
print(15//4)
#output: 3
print(-15//4)
#output: -4 >> rounding to the lowest